numero = int(input("Ingresa un número entero: "))
i = 1
suma = 0

# Sumar los 100 números siguientes
while i <= 100:
    suma += numero + i
    i += 1

# Mostrar el resultado
print("La suma de los 100 números siguientes a", numero,  "es:", suma)